MATRIX_MAP = {
    'blosum45': BLOSUM45,
    'blosum62': BLOSUM62,
    'blosum80': BLOSUM80,
    'pam30': PAM30,
    'pam70': PAM70,
    'pam250': PAM250
}


class ScoringMatrix:
    """
    打分矩阵类
    支持DNA简单打分、内置蛋白质矩阵和自定义矩阵文件
    """
    
    def __init__(self, seq_type='dna', match=1, mismatch=-1, gap=-2,
                 matrix_name=None, matrix_file=None):
        self.seq_type = seq_type
        self.match = match
        self.mismatch = mismatch
        self.gap = gap
        self.matrix_name = matrix_name
        self.matrix = None
        
        if seq_type == 'protein' or matrix_name or matrix_file:
            if matrix_file:
                self._load_custom_matrix(matrix_file)
            elif matrix_name:
                matrix_lower = matrix_name.lower()
                if matrix_lower in MATRIX_MAP:
                    self.matrix = MATRIX_MAP[matrix_lower]
                else:
                    raise ValueError(f"未知的打分矩阵: {matrix_name}")
            else:
                self.matrix = BLOSUM62
                self.matrix_name = 'blosum62'
    
    def _load_custom_matrix(self, matrix_file):
        """
        从文件加载自定义打分矩阵
        格式: 
        # 注释行
        A T C G
        A 5 -2 -2 -2
        T -2 5 -2 -2
        C -2 -2 5 -2
        G -2 -2 -2 5
        """
        try:
            with open(matrix_file, 'r', encoding='utf-8') as f:
                lines = [line.strip() for line in f if line.strip() and not line.startswith('#')]
            
            if not lines:
                raise ValueError("矩阵文件为空")
            
            residues = lines[0].split()
            n = len(residues)
            self.matrix = {}
            
            for i in range(n):
                parts = lines[i + 1].split()
                if len(parts) != n + 1:
                    raise ValueError(f"矩阵第{i+2}行格式错误，应有{n+1}个元素")
                residue = parts[0]
                values = parts[1:]
                if residue not in residues:
                    raise ValueError(f"未知的残基: {residue}")
                self.matrix[residue] = {}
                for j, value in enumerate(values):
                    self.matrix[residue][residues[j]] = int(value)
            
            self.matrix_name = os.path.basename(matrix_file)
            print(f"成功加载自定义矩阵: {self.matrix_name}")
        except Exception as e:
            print(f"加载自定义矩阵文件时出错: {e}")
            self.matrix = BLOSUM62
            self.matrix_name = 'blosum62'
    
    def get_score(self, a, b):
        """
        获取两个残基的比对得分
        """
        if a == '-' or b == '-':
            return self.gap
        
        a = a.upper()
        b = b.upper()
        
        if self.matrix:
            try:
                return self.matrix[a][b]
            except KeyError:
                try:
                    return self.matrix[b][a]
                except KeyError:
                    return self.mismatch
        
        return self.match if a == b else self.mismatch
    
    def __str__(self):
        if self.matrix_name:
            return f"ScoringMatrix({self.seq_type}, matrix={self.matrix_name}, gap={self.gap})"
        return f"ScoringMatrix({self.seq_type}, match={self.match}, mismatch={self.mismatch}, gap={self.gap})"
